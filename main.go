func CalculateAAndB3By3(C [][]float64) (A, B [][]float64, err error) {
	A = [][]float64{{1, 0, 0}, {0, 1, 0}, {0, 0, 1}}
	B = [][]float64{{0, 0, 0}, {0, 0, 0}, {0, 0, 0}}

	B[0][0] = C[0][0]
	B[0][1] = C[0][1]
	B[0][2] = C[0][2]

	A[1][0] = C[1][0] / B[0][0]
	B[1][1] = C[1][1] - A[1][0]*B[0][1]
	B[1][2] = C[1][2] - A[1][0]*B[0][2]

	A[2][0] = C[2][0] / B[0][0]
	A[2][1] = C[2][1] / B[0][1]
	B[2][2] = C[2][2] - A[2][0]*B[0][2] - A[2][1]*B[1][2]

	return
}
